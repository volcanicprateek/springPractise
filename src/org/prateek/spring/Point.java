public class Point
{
    private String x;
    private String y;

    /**
     * @return the x
     */
    public String getX()
    {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(final String x)
    {
        this.x = x;
    }

    /**
     * @return the y
     */
    public String getY()
    {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(final String y)
    {
        this.y = y;
    }

}
