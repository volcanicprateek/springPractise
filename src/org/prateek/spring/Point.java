package org.prateek.spring;

/**
 * 
 */
public class Point
{
    private int x;
    private int y;

    /**
     * @return the x
     */
    public int getX()
    {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(final int x)
    {
        this.x = x;
    }

    /**
     * @return the y
     */
    public int getY()
    {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(final int y)
    {
        this.y = y;
    }

}
